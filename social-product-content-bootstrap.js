"use strict";

const Module = require("module");
const { CAMPAIGN_ID } = require("./social-product-content-rebuild");

const VERSION = "2.0.0";

const status = {
  ok: false,
  state: "waiting",
  version: VERSION,
  campaignId: CAMPAIGN_ID,
  autoGeneratedScheduleEnabled: false,
  activeTotal: 0,
  removedAutoGenerated: 0,
  preservedPublished: 0,
  error: "",
  updatedAt: new Date().toISOString(),
};

function setStatus(change) {
  Object.assign(status, change, { updatedAt: new Date().toISOString() });
}

function currentHealth(readStore) {
  const store = readStore();
  const posts = Array.isArray(store.posts) ? store.posts : [];
  const activeAutoGenerated = posts.filter(
    (post) => post.campaignId === CAMPAIGN_ID && post.status !== "published"
  );
  const published = posts.filter(
    (post) => post.campaignId === CAMPAIGN_ID && post.status === "published"
  );

  return {
    ok: activeAutoGenerated.length === 0,
    state: activeAutoGenerated.length === 0 ? "disabled" : "cleanup-needed",
    version: VERSION,
    campaignId: CAMPAIGN_ID,
    autoGeneratedScheduleEnabled: false,
    activeTotal: activeAutoGenerated.length,
    preservedPublished: published.length,
    total: posts.length,
    updatedAt: new Date().toISOString(),
  };
}

function removeAutoGeneratedSchedule(readStore, writeStore) {
  const store = readStore();
  const posts = Array.isArray(store.posts) ? store.posts : [];
  const removable = posts.filter(
    (post) => post.campaignId === CAMPAIGN_ID && post.status !== "published"
  );
  const preservedPublished = posts.filter(
    (post) => post.campaignId === CAMPAIGN_ID && post.status === "published"
  ).length;

  if (removable.length) {
    writeStore({
      ...store,
      posts: posts.filter(
        (post) => post.campaignId !== CAMPAIGN_ID || post.status === "published"
      ),
    });
  }

  return {
    autoGeneratedScheduleEnabled: false,
    activeTotal: 0,
    removedAutoGenerated: removable.length,
    preservedPublished,
  };
}

let installed = false;
function install() {
  if (installed) return;
  installed = true;

  const originalLoad = Module._load;
  Module._load = function patchedLoad(request, parent, isMain) {
    const loaded = originalLoad.apply(this, arguments);
    if (request === "./social-server" && parent?.filename?.endsWith("internal-entry.js") && loaded?.app) {
      loaded.app.get("/internal/product-content-healthz", (_req, res) => {
        const health = currentHealth(loaded.readStore);
        res.status(health.ok ? 200 : 503).json(health);
      });

      setImmediate(() => {
        try {
          setStatus({ state: "cleaning", error: "" });
          const result = removeAutoGeneratedSchedule(loaded.readStore, loaded.writeStore);
          setStatus({ ok: true, state: "disabled", ...result, error: "" });
          console.log("Auto-generated product social schedule disabled", result);
        } catch (error) {
          setStatus({
            ok: false,
            state: "failed",
            error: error.message || "auto-generated schedule cleanup failed",
          });
          console.error("Auto-generated product social schedule cleanup failed", error);
        }
      });
    }
    return loaded;
  };
}

install();

module.exports = {
  VERSION,
  CAMPAIGN_ID,
  status,
  currentHealth,
  removeAutoGeneratedSchedule,
  install,
};
